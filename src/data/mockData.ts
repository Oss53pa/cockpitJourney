export function userById(id: string): User {
  return users.find((u) => u.id === id) || users[0];
}
export function projectById(id: string): Project | undefined {
  return projects.find((p) => p.id === id);
}
export function sectionsByProject(projectId: string): Section[] {
  return sections.filter((s) => s.projectId === projectId).sort((a, b) => a.position - b.position);
}
export function tasksByProject(projectId: string): Task[] {
  return tasks.filter((t) => t.projectId === projectId);
}
export function tasksDueToday(): Task[] {
  const todayStr = new Date().toDateString();
  return tasks.filter((t) => t.dueDate && new Date(t.dueDate).toDateString() === todayStr);
}
